#include <unistd.h>

void	ft_putchar(char c)
{
	write(1, &c, 1);
}

void	ft_putstr(char *str)
{
	int i;

	i = 0;
	while (str[i])
	{
		write(1, &str[i], 1);
		i++;
	}
}

int		main(int argc, char **argv)
{
	int count;

	count = argc - 1;
	while (count > 0)
	{
		ft_putstr(argv[count]);
		ft_putchar('\n');
		count--;
	}
	return (0);
}
