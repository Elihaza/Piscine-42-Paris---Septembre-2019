int		ft_sqrt(int nb)
{
	int i;
	int root;

	i = 1;
	root = 1;
	if (nb <= 0)
		return (0);
	if (nb == 2)
		return (0);
	while (i < nb / i)
		i++;
	if (nb % i == 0)
		return (i);
	else
		return (0);
}
